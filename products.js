// Função para filtrar produtos
export const filterProducts = (products, filters) => {
  return products.filter(product => {
    const matchesSearch = !filters.search || 
      product.name.toLowerCase().includes(filters.search.toLowerCase()) ||
      product.description.toLowerCase().includes(filters.search.toLowerCase());
    
    const matchesCategory = !filters.category || filters.category === 'all' || 
      product.category === filters.category;
    
    const matchesOccasion = !filters.occasion || filters.occasion === 'all' || 
      product.occasion === filters.occasion;
    
    const matchesSize = !filters.size || filters.size === 'all' || 
      product.sizes.includes(filters.size);
    
    let matchesPrice = true;
    if (filters.priceRange && filters.priceRange !== 'all') {
      const [min, max] = filters.priceRange.split('-').map(p => p === '+' ? Infinity : parseInt(p));
      matchesPrice = product.price >= min && (max ? product.price <= max : true);
    }
    
    const matchesColor = !filters.color || filters.color === 'all' || 
      product.colors.some(color => color.toLowerCase().includes(filters.color.toLowerCase()));
    
    return matchesSearch && matchesCategory && matchesOccasion && matchesSize && matchesPrice && matchesColor;
  });
};

// Função para obter produtos relacionados
export const getRelatedProducts = (currentProduct, allProducts, limit = 4) => {
  return allProducts
    .filter(product => 
      product.id !== currentProduct.id && 
      (product.category === currentProduct.category || product.occasion === currentProduct.occasion)
    )
    .slice(0, limit);
};

// Função para obter produtos em destaque
export const getFeaturedProducts = (allProducts, limit = 6) => {
  return allProducts
    .filter(product => product.rating >= 4.5)
    .sort((a, b) => b.reviews - a.reviews)
    .slice(0, limit);
};

